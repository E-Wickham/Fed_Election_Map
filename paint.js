function calcResize() {
    let hWin = window.innerHeight;
    let hScreen = window.screen.height
    let wScreen = window.screen.width
    console.log("screen ", hScreen)
    let mapWin = document.getElementById("canada_map")
    let parWin = mapWin.parentNode;
    let parWinNums = mapWin.parentNode.getBoundingClientRect();
    console.log(parWinNums.height)
    console.log(hWin)
    let dataWin = document.querySelector(".data_holder")
    //If screen less than 1200px, stack elements on top of each other, make everything full width
    
    if (hScreen < 1080 && parWinNums.width < 600) {
        mapWin.style.height = `${hScreen/2}px`
        mapWin.style.width = `${wScreen}px`
        dataWin.style.width = `${parWinNums.width-55}px`
    }
    else if (parWinNums.width < 1200) {
        mapWin.style.width = `${parWinNums.width+20}px`
        dataWin.style.width = `${parWinNums.width-55}px`
    }
    else if (parWinNums.height > hWin && parWinNums.width) {
        mapWin.style.height = `${hWin-300}px`
    }

    //width="900" height="868" viewBox="0 0 800 868"
}

